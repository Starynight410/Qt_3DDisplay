#ifndef BYTE_ORDER_FUNCTIONS_H
#define BYTE_ORDER_FUNCTIONS_H

#include <cstdint>
#include <cstring>

// Byte order macros
#if __BYTE_ORDER == __LITTLE_ENDIAN
#  define ltohl(x)       (x)
#  define ltohs(x)       (x)
#  define htoll(x)       (x)
#  define htols(x)       (x)
#elif __BYTE_ORDER == __BIG_ENDIAN
#  define ltohl(x)     __bswap_32(x)
#  define ltohs(x)     __bswap_16(x)
#  define htoll(x)     __bswap_32(x)
#  define htols(x)     __bswap_16(x)
#endif

#endif // BYTE_ORDER_FUNCTIONS_H
